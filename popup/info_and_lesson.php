function add_lesson($sisi, $lessons, &$info){
  foreach ($info as &$student){
    if($student['sisi']==$sisi){
      foreach ($lessons as $lesson) {
        $student['lessons'][]=$lesson;
      }
      return true;
    }
  }
  return false;
}


function search_student($search_val,$info){
  $result = array();
  foreach ($info as $student) {
    if($student['firstname'] == $search_val || $student['sisi']==$search_val){
      $result[]=$student;
    }
  }
  return $result;
}
//UTF-8 үсэгний ардах 10 тийн тоог авна.
function ordinal($str){
   $charString = mb_substr($str, 0, 1, 'utf-8');
   $size = strlen($charString);
   $ordinal = ord($charString[0]) & (0xFF >> $size);
   //Merge other characters into the value
   for($i = 1; $i < $size; $i++){
       $ordinal = $ordinal << 6 | (ord($charString[$i]) & 127);
   }
   return $ordinal;
 }

